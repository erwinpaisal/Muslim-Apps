package com.erwinpaisal.muslimapps.model;

public class SurahNameModel {

    private int noSurah;
    private String nameSurah, nameSurahIndo;

    public void setNameSurahIndo(String nameSurahIndo) {
        this.nameSurahIndo = nameSurahIndo;
    }

    public String getNameSurahIndo() {
        return nameSurahIndo;
    }

    public int getNoSurah() {
        return noSurah;
    }

    public void setNoSurah(int noSurah) {
        this.noSurah = noSurah;
    }

    public String getNameSurah() {
        return nameSurah;
    }

    public void setNameSurah(String nameSurah) {
        this.nameSurah = nameSurah;
    }
}

package com.erwinpaisal.muslimapps.model;

public class WilayahModel {
    public int getIdWilayah() {
        return idWilayah;
    }

    public void setIdWilayah(int idWilayah) {
        this.idWilayah = idWilayah;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getKodeWilayah() {
        return kodeWilayah;
    }

    public void setKodeWilayah(String kodeWilayah) {
        this.kodeWilayah = kodeWilayah;
    }

    public String getMstWilayah() {
        return mstWilayah;
    }

    public void setMstWilayah(String mstWilayah) {
        this.mstWilayah = mstWilayah;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    private int idWilayah,level;
    private String kodeWilayah,mstWilayah,nama;
}
